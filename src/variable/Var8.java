//변수 타입 _ 정수는 int/long, 실수는 double 사용
package variable;

public class Var8 {
    public static void main(String[] args) {
        //정수
        byte b = 127; //-128 ~ 127
        short s = 32767; // -32,768 ~ 32,767
        int i = 2147483647; // -2147483648 ~ 2,147483647(약 20억)
        long l  = 9223372036854775807L;

        //실수
        float f = 10.0f;
        double d = 10.0;
    }
}
